; -----------------------------------------------------------
; -------------------------- NMI ----------------------------
; -----------------------------------------------------------
; Reference guide: https://www.nesdev.org/wiki/NMI
nmi:
  LDA #$00
  STA OAMADDR
  LDA #$02
  STA OAMDMA

  LDA figure
  STA $0233
  STA $023B
  TAX
  CLC
  ADC #$08
  STA $0237
  STA $023F
  INX
  STX figure

  RTI

